# Даны целые положительные числа A и B (A > B). На отрезке длины
# A размещено максимально возможное количество отрезков длины B (без наложений).
# Используя операцию взятия остатка от деления нацело, найти длину незанятой части
# отрезка A.
try:
    A = int(input("Введите длину отрезка A: "))
    B = int(input("Введите длину отрезка B: "))

    # Проверяем, что длина отрезка B больше нуля
    if B <= 0:
        print('Длина отрезка B должна быть больше нуля. Попробуйте снова.')
    # Проверяем условие, что A и B положительно
    elif A > B > 0:
        # Находим остаток от деления A на B
        Del = A % B
        # Выводим количество свободного пространства в отрезке A
        print("В отрезке A не занято", Del)
    else:
        # Выводим сообщение об ошибке при неверных значениях
        print('Вы ввели неправильное значение, попробуйте снова.')
except ValueError:
    # Обрабатываем случай, когда введено не целое число
    print('Ошибка ввода! Пожалуйста, введите целые положительные числа.')
